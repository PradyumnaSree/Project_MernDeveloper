package Mern_Project;

public class LoginBean 
{
	private int F_SNO;
	private String F_USERNAME, F_PWORD;
	public int getF_SNO() {
		return F_SNO;
	}
	public void setF_SNO(int f_SNO) {
		F_SNO = f_SNO;
	}
	public String getF_USERNAME() {
		return F_USERNAME;
	}
	public void setF_USERNAME(String f_USERNAME) {
		F_USERNAME = f_USERNAME;
	}
	public String getF_PWORD() {
		return F_PWORD;
	}
	public void setF_PWORD(String f_PWORD) {
		F_PWORD = f_PWORD;
	}
	
}
